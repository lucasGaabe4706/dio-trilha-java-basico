/*BREAK
 * Break significa quebrar, parar, frear, interromper. E é isso que se faz quando o java encontra esse comando pela frente. CONTINUE, como o nome diz, continua o laço. O comando BREAK interrmope o laço, já o CONTINUE interrompe somente a iteração atual.
 */

public class ExemploBreakContinue {
    public static void main(String[] args) {
	
        for(int numero = 1; numero <=5; numero ++){
            if(numero==3)
                break;
            
            System.out.println(numero);
            
        }
        //Qual a saída no console ?
    
        }
    }

